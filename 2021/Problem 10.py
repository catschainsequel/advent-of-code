def get_corruption_score(line):
    # print ("line:" + line)
    unclosed_terminators = []
    for char in line:
        if char in ["(", "[", "{", "<"]:
            unclosed_terminators.append(char)
        elif char == ")":
            if unclosed_terminators.pop() != "(":
                return 3
        elif char == "]":
            if unclosed_terminators.pop() != "[":
                return 57
        elif char == "}":
            if unclosed_terminators.pop() != "{":
                return 1197
        elif char == ">":
            if unclosed_terminators.pop() != "<":
                return 25137
    return 0

def get_completion_score(line):
    unclosed_terminators = []
    for char in line:
        if char in ["(", "[", "{", "<"]:
            unclosed_terminators.append(char)
        elif char == ")":
            if unclosed_terminators.pop() != "(":
                raise "WTF"
        elif char == "]":
            if unclosed_terminators.pop() != "[":
                raise "WTF"
        elif char == "}":
            if unclosed_terminators.pop() != "{":
                raise "WTF"
        elif char == ">":
            if unclosed_terminators.pop() != "<":
                raise "WTF"
    
    completion_score = 0
    for completion_char in reversed(unclosed_terminators):
        completion_score *= 5
        if completion_char == "(":
            completion_score += 1
        elif completion_char == "[":
            completion_score += 2
        elif completion_char == "{":
            completion_score += 3
        elif completion_char == "<":
            completion_score += 4
    
    return completion_score


